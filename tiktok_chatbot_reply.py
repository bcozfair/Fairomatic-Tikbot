def get_best_reply(text):
    for key in auto_replies:
        similarity = fuzz.partial_ratio(key, text)
        if similarity > 80:  # ‡∏ñ‡πâ‡∏≤‡∏Ñ‡∏ß‡∏≤‡∏°‡∏Ñ‡∏•‡πâ‡∏≤‡∏¢‡∏Å‡∏±‡∏ô‡∏°‡∏≤‡∏Å‡∏Å‡∏ß‡πà‡∏≤ 80%
            return auto_replies[key]
    return None

